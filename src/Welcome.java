    private void fetch(){
        DefaultTableModel m = (DefaultTableModel) table.getModel();
        int rows = m.getRowCount();
                if(rows > 0){
                    for (int i = 0 ; i < rows ; i++){
                        m.removeRow(0);
                    }
                }
        List<Data> datas = dataS.queryData(username);
        if(datas == null)
            System.out.println("NO DATA");
        else
        {
            for (Iterator<Data> it = datas.iterator(); it.hasNext();) {
                Data data = it.next();
                m.addRow(new Object[] {data.getWebsite(),data.getEmail(),data.getUsername(),data.getPhone()});
            }} 
    }
    
    
